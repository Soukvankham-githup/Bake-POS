let currentLang = localStorage.getItem('bb_lang') || 'la';

function t(key) {
    return (translations[currentLang] && translations[currentLang][key]) || key;
}

function switchLang(lang) {
    currentLang = lang;
    localStorage.setItem('bb_lang', lang);
    updateUILanguage();
    renderCategories();
    filterProducts();
    renderCart();
    updateStatus();
}

function updateUILanguage() {
    document.getElementById('posTitle').textContent = t('posTitle');
    document.getElementById('onlineText').textContent = t('online');
    document.getElementById('historyBtnText').textContent = t('history');
    document.getElementById('searchInput').placeholder = t('searchPlaceholder');
    document.getElementById('noResultsText').textContent = t('noResults');
    document.getElementById('cartTitleText').textContent = t('cart');
    document.getElementById('clearCartBtn').innerHTML = '<i class="fas fa-trash-alt"></i> ' + t('clear');
    document.getElementById('discountInput').placeholder = t('discountPlaceholder');
    document.getElementById('discountBtn').textContent = t('applyDiscount');
    document.getElementById('emptyCartMain').textContent = t('emptyCartTitle');
    document.getElementById('emptyCartSub').textContent = t('emptyCartSub');
    document.getElementById('subtotalLabel').textContent = t('subtotal');
    document.getElementById('discountLabel').textContent = t('discountLabel');
    document.getElementById('totalLabel').textContent = t('total');
    document.getElementById('holdBtnText').textContent = t('holdBill');
    document.getElementById('payBtnText').textContent = t('pay');
    document.getElementById('billsTodayLabel').innerHTML = '<i class="fas fa-receipt"></i> ' + t('billsToday');
    document.getElementById('salesTodayLabel').innerHTML = '<i class="fas fa-coins"></i> ' + t('salesToday');
    document.getElementById('statusRight').textContent = t('statusRight');
    document.querySelectorAll('.lang-btn').forEach(function (btn) {
        btn.classList.toggle('active', btn.dataset.lang === currentLang);
        document.getElementById('logoutText').textContent = t('logout');
    });
}